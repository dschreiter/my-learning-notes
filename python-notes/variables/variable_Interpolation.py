
"""
In Python, variable interpolation is commonly done using f-strings, 
which were introduced in Python 3.6. 
An f-string is a string literal that is prefixed with 'f' or 'F' 
and can include expressions inside curly braces {}. 
Here's an example of variable interpolation:
"""

name = "Alice"
age = 30

# Using f-string for variable interpolation
message = f"Hello, my name is {name} and I am {age} years old."


print(message)

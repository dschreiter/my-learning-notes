# Here is a list of some common built-in exception classes in Python:

# Exception: The base class for all built-in exceptions.
# AttributeError: Raised when an attribute reference or assignment fails.
# TypeError: Raised when an operation or function is applied to an object of an inappropriate type.
# ValueError: Raised when a built-in operation or function receives an argument of the correct type but an inappropriate value.
# IndexError: Raised when a sequence subscript is out of range.
# KeyError: Raised when a dictionary key is not found.
# FileNotFoundError: Raised when a file or directory is requested but cannot be found.
# OSError: A base class for I/O and system-related errors.
# NameError: Raised when a local or global name is not found.
# ZeroDivisionError: Raised when the second operand of a division or modulo operation is zero.
# SyntaxError: Raised when the parser encounters a syntax error.
# IndentationError: Raised when there is incorrect indentation.
# ImportError: Raised when an import statement fails to find the specified module.
# KeyError: Raised when a dictionary key is not found.
# ValueError: Raised when a function receives an argument of the correct type but an inappropriate value.
# RuntimeError: Raised when an error is detected that doesn't fall into any of the other categories.
# This is not an exhaustive list, and there are other built-in exceptions as well. You can find the complete list in the Python documentation: Built-in Exceptions.